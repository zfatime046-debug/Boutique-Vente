    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        int row = tableProduits.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un produit dans la table !");
            return;
        }

        int id = Integer.parseInt(tableProduits.getValueAt(row, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Voulez-vous vraiment supprimer ce produit ?",
            "Confirmation",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            ProduitService.delete(id);  
            loadTable();
            JOptionPane.showMessageDialog(this, "Produit supprimé !");
        }

    }//GEN-LAST:event_btnSupprimerActionPerformed


