    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        String lib = txtLibelle.getText().trim();
        String prixTxt = txtPrix.getText().trim();
        String stockTxt = txtStock.getText().trim();

        if (lib.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Libellé obligatoire !");
            return;
        }
        if (comboCategorie.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Choisissez une catégorie !");
            return;
        }

        double prix;
        int stock;

        try {
            prix = Double.parseDouble(prixTxt);
            stock = Integer.parseInt(stockTxt);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Prix ou stock invalide !");
            return;
        }

        if (prix <= 0) {
            JOptionPane.showMessageDialog(this, "Le prix doit être > 0 !");
            return;
        }
        if (stock < 0) {
            JOptionPane.showMessageDialog(this, "Le stock doit être >= 0 !");
            return;
        }

        try {
            Produit p = new Produit();
            p.setLibelle(lib);
            p.setPrix(prix);
            p.setStock(stock);
            p.setCategorie(comboCategorie.getSelectedItem().toString());

            ProduitService.create(p);

            JOptionPane.showMessageDialog(this, "Produit ajouté avec succès !");
            loadTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout : " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed

        int row = tableProduits.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un produit dans la table !");
            return;
        }

        String lib = txtLibelle.getText().trim();
        String prixTxt = txtPrix.getText().trim();
        String stockTxt = txtStock.getText().trim();

        if (lib.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Libellé obligatoire !");
            return;
        }
        if (comboCategorie.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Choisissez une catégorie !");
            return;
        }

        double prix;
        int stock;
        try {
            prix = Double.parseDouble(prixTxt);
            stock = Integer.parseInt(stockTxt);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Prix ou stock invalide !");
            return;
        }

        if (prix <= 0) {
            JOptionPane.showMessageDialog(this, "Le prix doit être > 0 !");
            return;
        }
        if (stock < 0) {
            JOptionPane.showMessageDialog(this, "Le stock doit être >= 0 !");
            return;
        }

        int id = Integer.parseInt(tableProduits.getValueAt(row, 0).toString());

        try {
            Produit p = new Produit();
            p.setIdProduit(id);
            p.setLibelle(lib);
            p.setPrix(prix);
            p.setStock(stock);
            p.setCategorie(comboCategorie.getSelectedItem().toString());

            ProduitService.update(p);
            loadTable();
            JOptionPane.showMessageDialog(this, "Produit modifié !");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la modification : " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnModifierActionPerformed

